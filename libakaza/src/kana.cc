std::wstring akaza::hira2kata(const std::wstring &src) {
    std::wstring buf;
    for (const auto &c: src) {
        if (hira2kata_map.count(c) > 0) {
            buf += hira2kata_map.at(c);
        } else {
            buf += c;
        }
    }
    return buf;
}
